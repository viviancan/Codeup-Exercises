	Create a function that takes in a string and echoes out how many occurences of each alpha-numeric character is present in the string.

	Example:

	The string "aabb131"

	Should yield the following output:

	This string contains...
	- (2) "a" characters
	- (2) "b" characters
	- (2) "1" characters
	- (1) "3" characters